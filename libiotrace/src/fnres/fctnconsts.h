/* djb2 by Dan Bernstein */
static uint64_t hash_string(const char *str) {
    unsigned long hash = 5381;
    int c;

    while ((c = *str++)) hash = ((hash << 5) + hash) + c;
    return hash;
}
#define SWITCH_FCTNAME(function_name) switch (hash_string(function_name))


